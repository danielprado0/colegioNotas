    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try {
            if(this.jcbCarr.getSelectedIndex() != -1 && this.jcbgrad.getSelectedIndex() != -1 && this.jcbsec.getSelectedIndex() != -1 && this.jcbcicl.getSelectedIndex() != -1 ){
                Conexion conect4 = new Conexion();
                ResultSet rs4; //)
            rs4 = conect4.consul("select count(asignacion.idAsignacion) from alumno inner join asignacion on Seccion_G_C_idSeccion_G_C = "+djcsec.getElementAt(this.jcbsec.getSelectedIndex())+" and Cicloesc_idCicloesc = "+this.djccicl.getElementAt(this.jcbcicl.getSelectedIndex()) +" where idAlumno = Alumno_idAlumno order by alumno.Apellido , alumno.Nombre  ;");
            rs4.next();
            int temp = rs4.getInt(1), inc=0;
            rs4 = conect4.consul("select asignacion.Clave, alumno.Apellido, alumno.Nombre, asignacion.idAsignacion, alumno.idAlumno from alumno inner join asignacion on Seccion_G_C_idSeccion_G_C = "+djcsec.getElementAt(this.jcbsec.getSelectedIndex())+" and Cicloesc_idCicloesc = "+this.djccicl.getElementAt(this.jcbcicl.getSelectedIndex()) +" where idAlumno = Alumno_idAlumno order by alumno.Apellido , alumno.Nombre  ;");
            Vector = new String[temp][3];
            asig.removeAllElements();
            idalum.removeAllElements();
            while (rs4.next()){
                idalum.addElement(rs4.getInt(5));
                Vector[inc][0]= String.valueOf(rs4.getInt(1));
                Vector[inc][1]= rs4.getString(2);
                Vector[inc][2]= rs4.getString(3);
                asig.addElement(rs4.getInt(4));
                inc++;
            }
            jTable1.setModel(new javax.swing.table.DefaultTableModel(
                Vector,
                new String [] {
                    "Clave", "Apellidos", "Nombres"
                }
            )
            {
                boolean[] canEdit = new boolean [] {
                    false, false, false
                };
                public boolean isCellEditable(int rowIndex, int columnIndex) {
                    return canEdit [columnIndex];
                }
            });

        }

        } catch (SQLException ex) {
            Logger.getLogger(Frmiexplorer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

